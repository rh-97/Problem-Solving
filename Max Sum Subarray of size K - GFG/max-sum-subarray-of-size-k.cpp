class Solution{   
public:
    long maximumSumSubarray(int K, vector<int> &Arr , int N){
        long sum = accumulate(Arr.begin(), Arr.begin()+K, long(0));
        long res = sum;
        for (int i = 0, j = K; j < N; ++i, ++j) {
            sum += Arr[j] - Arr[i];
            
            res = max(res, sum);
        }
        
        return res;
    }
};

